int	test_6(void)
{
	void	*arr[100];
	int		i;
	int		j;

	i = -1;
	while (++i < 10)
	{
		j = 0;
		while (j < 100)
		{
			arr[j] = malloc(128);
			if (!arr[j])
				return (1);
			j++;
		}
		j = 0;
		while (j < 100)
		{
			free(arr[j]);
			j++;
		}
	}
	return (0);
}
